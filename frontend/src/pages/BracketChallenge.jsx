// frontend/src/pages/BracketChallenge.jsx
import { useEffect, useState, useCallback } from 'react';
import MatchupCard from '../components/MatchupCard';
import { predictMatch, submitUserPrediction } from '../api/client';

// FIFA World Cup 2026 — Round of 32 (full 32-team knockout bracket)
const INITIAL_R32 = [
  { id: 'r32_1',  team1: 'France',      team2: 'Scotland' },
  { id: 'r32_2',  team1: 'Brazil',      team2: 'Ghana' },
  { id: 'r32_3',  team1: 'Argentina',   team2: 'Saudi Arabia' },
  { id: 'r32_4',  team1: 'Portugal',    team2: 'Canada' },
  { id: 'r32_5',  team1: 'Spain',       team2: 'Turkey' },
  { id: 'r32_6',  team1: 'Germany',     team2: 'Iran' },
  { id: 'r32_7',  team1: 'England',     team2: 'Serbia' },
  { id: 'r32_8',  team1: 'Netherlands', team2: 'Ecuador' },
  { id: 'r32_9',  team1: 'Belgium',     team2: 'Australia' },
  { id: 'r32_10', team1: 'Italy',       team2: 'Nigeria' },
  { id: 'r32_11', team1: 'Uruguay',     team2: 'USA' },
  { id: 'r32_12', team1: 'Colombia',    team2: 'Mexico' },
  { id: 'r32_13', team1: 'Croatia',     team2: 'Switzerland' },
  { id: 'r32_14', team1: 'Denmark',     team2: 'South Korea' },
  { id: 'r32_15', team1: 'Morocco',     team2: 'Senegal' },
  { id: 'r32_16', team1: 'Japan',       team2: 'Poland' },
];

const ROUNDS = ['Round of 32', 'Round of 16', 'Quarter-finals', 'Semi-finals', 'Final'];
const TOTAL_MATCHUPS = 16 + 8 + 4 + 2 + 1; // 31

// card height + gap ≈ 82px — used to compute vertical bracket spacing
const CELL = 82;

function buildEmptyRound(n, prefix) {
  return Array.from({ length: n }, (_, i) => ({
    id: `${prefix}_${i + 1}`, team1: null, team2: null, prob1: null, prob2: null,
  }));
}

function ModelBanner() {
  const stats = [
    { label: 'Training matches', value: '49,287' },
    { label: 'Model', value: 'XGBoost' },
    { label: 'Features', value: '9' },
    { label: '2018 WC accuracy', value: '40.6%' },
    { label: '2022 WC accuracy', value: '39.1%' },
    { label: 'Baseline (random)', value: '33.3%' },
  ];
  return (
    <div className="border border-gray-800 rounded-2xl bg-gray-900/60 p-6 mb-8">
      <div className="flex flex-wrap items-start justify-between gap-6">
        <div className="max-w-lg">
          <p className="text-xs font-bold uppercase tracking-widest text-emerald-500 mb-1">Powered by ML</p>
          <h2 className="text-lg font-bold text-white mb-2">How the predictions work</h2>
          <p className="text-sm text-gray-400 leading-relaxed">
            Each matchup probability is generated by an XGBoost classifier trained on 49,287 international results from 1872–2024. Features include ELO ratings (tournament-weighted), recent form (last 5 matches), and head-to-head records. All World Cup matches are treated as neutral-ground.
          </p>
        </div>
        <div className="flex flex-wrap gap-3">
          {stats.map(s => (
            <div key={s.label} className="bg-gray-800 border border-gray-700 rounded-xl px-4 py-3 text-center min-w-[90px]">
              <div className="text-lg font-bold text-emerald-400">{s.value}</div>
              <div className="text-[11px] text-gray-500 mt-0.5">{s.label}</div>
            </div>
          ))}
        </div>
      </div>
    </div>
  );
}

export default function BracketChallenge() {
  const [username, setUsername] = useState('');
  const [submitted, setSubmitted] = useState(false);
  const [submitting, setSubmitting] = useState(false);
  const [submitMsg, setSubmitMsg] = useState('');

  const [bracket, setBracket] = useState([
    INITIAL_R32.map(m => ({ ...m, prob1: null, prob2: null })),
    buildEmptyRound(8, 'r16'),
    buildEmptyRound(4, 'qf'),
    buildEmptyRound(2, 'sf'),
    buildEmptyRound(1, 'f'),
  ]);

  const [picks, setPicks] = useState({});
  const [loadingProbs, setLoadingProbs] = useState({});

  const fetchProbs = useCallback(async (matchupId, team1, team2) => {
    if (!team1 || !team2) return;
    setLoadingProbs(p => ({ ...p, [matchupId]: true }));
    try {
      const { data } = await predictMatch(team1, team2, true);
      setBracket(prev => prev.map(round =>
        round.map(m =>
          m.id === matchupId
            ? { ...m, prob1: data.prob_home_win, prob2: data.prob_away_win }
            : m
        )
      ));
    } catch {
      // silently fail
    } finally {
      setLoadingProbs(p => ({ ...p, [matchupId]: false }));
    }
  }, []);

  useEffect(() => {
    INITIAL_R32.forEach(m => fetchProbs(m.id, m.team1, m.team2));
  }, [fetchProbs]);

  const handlePick = useCallback((matchupId, winner) => {
    setPicks(prev => ({ ...prev, [matchupId]: winner }));

    setBracket(prev => {
      const next = prev.map(r => r.map(m => ({ ...m })));

      for (let roundIdx = 0; roundIdx < next.length - 1; roundIdx++) {
        const matchIdx = next[roundIdx].findIndex(m => m.id === matchupId);
        if (matchIdx === -1) continue;

        const nextMatchIdx = Math.floor(matchIdx / 2);
        const slot = matchIdx % 2 === 0 ? 'team1' : 'team2';
        const nextMatchup = next[roundIdx + 1][nextMatchIdx];

        if (nextMatchup[slot] !== winner) {
          nextMatchup[slot] = winner;
          nextMatchup.prob1 = null;
          nextMatchup.prob2 = null;

          setPicks(p => {
            const updated = { ...p };
            delete updated[nextMatchup.id];
            return updated;
          });

          if (nextMatchup.team1 && nextMatchup.team2) {
            setTimeout(() => fetchProbs(nextMatchup.id, nextMatchup.team1, nextMatchup.team2), 0);
          }
        }
        break;
      }
      return next;
    });
  }, [fetchProbs]);

  const handleSubmit = async () => {
    if (!username.trim()) return setSubmitMsg('Enter a username first');
    if (Object.keys(picks).length < TOTAL_MATCHUPS) {
      return setSubmitMsg(`Complete all ${TOTAL_MATCHUPS} picks first (${Object.keys(picks).length}/${TOTAL_MATCHUPS} done)`);
    }
    setSubmitting(true);
    setSubmitMsg('');
    try {
      const allMatchups = bracket.flat();
      await Promise.all(
        Object.entries(picks).map(([matchupId, winner], idx) => {
          const matchup = allMatchups.find(m => m.id === matchupId);
          const outcome = matchup && matchup.team2 === winner ? 'away_win' : 'home_win';
          return submitUserPrediction(username.trim(), idx + 1, outcome).catch(() => null);
        })
      );
      setSubmitted(true);
      setSubmitMsg('');
    } catch {
      setSubmitMsg('Something went wrong. Try again.');
    } finally {
      setSubmitting(false);
    }
  };

  const totalPicks = Object.keys(picks).length;
  const champion = picks[bracket[4][0]?.id];

  // Vertical spacing formula: each bracket level doubles the cell size
  const bracketGap = (roundIdx) =>
    roundIdx === 0 ? 6 : Math.pow(2, roundIdx) * CELL - 76;
  const bracketPadTop = (roundIdx) =>
    roundIdx === 0 ? 0 : Math.pow(2, roundIdx) * CELL / 2 - 38;

  return (
    <div className="max-w-none">
      {/* Page title */}
      <div className="max-w-5xl mx-auto mb-6">
        <p className="text-xs font-bold uppercase tracking-widest text-emerald-500 mb-1">FIFA World Cup 2026</p>
        <h1 className="text-3xl font-extrabold text-white mb-1">AI Bracket Challenge</h1>
        <p className="text-gray-400 text-sm">
          Pick the winner of every knockout match — from Round of 32 to the Final. AI win probabilities powered by XGBoost trained on 49k historical matches.
        </p>
      </div>

      {/* Model explanation banner */}
      <div className="max-w-5xl mx-auto">
        <ModelBanner />
      </div>

      {/* Username + progress + submit */}
      <div className="max-w-5xl mx-auto mb-8 flex flex-wrap items-center gap-3 bg-gray-900 border border-gray-800 rounded-2xl px-5 py-4">
        <input
          type="text"
          placeholder="Your username"
          value={username}
          onChange={e => setUsername(e.target.value)}
          disabled={submitted}
          className="bg-gray-800 border border-gray-700 rounded-xl px-4 py-2.5 text-white text-sm w-52 focus:outline-none focus:ring-2 focus:ring-emerald-500 disabled:opacity-50"
        />

        {/* Progress bar */}
        <div className="flex items-center gap-2 flex-1 min-w-48">
          <div className="flex-1 h-2 bg-gray-800 rounded-full overflow-hidden">
            <div
              className="h-full bg-emerald-500 rounded-full transition-all"
              style={{ width: `${(totalPicks / TOTAL_MATCHUPS) * 100}%` }}
            />
          </div>
          <span className="text-sm text-gray-400 whitespace-nowrap">{totalPicks}/{TOTAL_MATCHUPS} picks</span>
        </div>

        {champion && (
          <span className="text-sm font-bold text-emerald-400 bg-emerald-900/30 border border-emerald-800 px-3 py-1 rounded-lg">
            🏆 {champion}
          </span>
        )}

        {!submitted ? (
          <button
            onClick={handleSubmit}
            disabled={submitting || totalPicks < TOTAL_MATCHUPS || !username.trim()}
            className="bg-emerald-600 hover:bg-emerald-500 disabled:bg-gray-800 disabled:text-gray-600 text-white font-semibold px-6 py-2.5 rounded-xl transition-colors text-sm whitespace-nowrap"
          >
            {submitting ? 'Submitting…' : 'Submit Bracket'}
          </button>
        ) : (
          <span className="text-emerald-400 font-semibold text-sm">✓ Bracket submitted!</span>
        )}
        {submitMsg && <span className="text-red-400 text-sm">{submitMsg}</span>}
      </div>

      {/* Bracket — horizontally scrollable */}
      <div className="overflow-x-auto pb-8">
        <div className="flex gap-6 min-w-max px-4" style={{ alignItems: 'flex-start' }}>
          {bracket.map((round, roundIdx) => (
            <div key={roundIdx} className="flex flex-col">
              {/* Round label */}
              <div className="text-[11px] font-bold text-gray-500 uppercase tracking-widest mb-3 text-center w-44">
                {ROUNDS[roundIdx]}
              </div>

              {/* Matchup cards with computed spacing */}
              <div
                className="flex flex-col"
                style={{
                  gap: `${bracketGap(roundIdx)}px`,
                  paddingTop: `${bracketPadTop(roundIdx)}px`,
                }}
              >
                {round.map(matchup => (
                  <MatchupCard
                    key={matchup.id}
                    matchup={matchup}
                    onPick={handlePick}
                    userPick={picks[matchup.id]}
                    loadingProbs={!!loadingProbs[matchup.id]}
                  />
                ))}
              </div>
            </div>
          ))}

          {/* Champion slot */}
          <div className="flex flex-col">
            <div className="text-[11px] font-bold text-gray-500 uppercase tracking-widest mb-3 text-center w-44">
              Champion
            </div>
            <div style={{ paddingTop: `${bracketPadTop(5)}px` }}>
              <div className={`w-44 rounded-xl border-2 p-5 text-center transition-all ${
                champion ? 'border-emerald-500 bg-emerald-900/20' : 'border-gray-700 bg-gray-900'
              }`}>
                <div className="text-3xl mb-2">🏆</div>
                <div className={`text-sm font-bold leading-snug ${champion ? 'text-emerald-300' : 'text-gray-600'}`}>
                  {champion || 'TBD'}
                </div>
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}
